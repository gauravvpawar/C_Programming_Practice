// perform the arithmetic operation between the number
#include<stdio.h>
int main()
{
    int a = 10;
    int b = 5;
    printf("Addition : %d\n" , a+b);
    printf("Subtraction : %d\n" , a-b);
    printf("Multiplication : %d\n" , a*b);
    printf("Divisor : %d\n" , a/b);
    printf("Modulo : %d\n",a%b);
    return 0;
}
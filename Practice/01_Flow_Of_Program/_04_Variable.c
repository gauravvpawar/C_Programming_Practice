// variable is like container
// to store the value in container

#include<stdio.h>
int main()
{
    // data type we have in c
    // byte short int long float double boolean

    int a = 5;
    printf("%d" , a);

    long long int large = 1234567890l;
    printf("%lld\n",large);

    float gravity = 9.8;
    printf("%f\n", gravity);

    char c = 'a';
    printf("%c\n", c);

    return 0;
}